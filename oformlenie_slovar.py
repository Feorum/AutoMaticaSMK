def _norm(s: str) -> str:
    """Нормализация для сравнения: нижний регистр, убрать точки/слэши/лишние пробелы."""
    s = s.lower()
    s = re.sub(r"[./\\,]", " ", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s


# Предрасчёт: нормализованный канон + нормализованные синонимы.
_KANON_NORM = {kanon: _norm(kanon) for kanon in OFORMLENIE_SINONIMY}
_SINONIMY_NORM = {
    kanon: sorted({_norm(kanon)} | {_norm(v) for v in syn}, key=len, reverse=True)
    for kanon, syn in OFORMLENIE_SINONIMY.items()
}


def izvlech_massu(text: str) -> tuple[str, str]:
    """Найти массу в граммах в тексте заказа.
    Возвращает (масса_строкой, текст_без_массы).
    Понимает: '650', '650г', '650 г', '0.5кг'→500, '0,65 кг'→650."""
    rest = text
    massa = ""

    # Сначала килограммы (0.5 кг, 0,65кг) → граммы
    m = re.search(r"(\d+[.,]?\d*)\s*кг\b", rest, re.IGNORECASE)
    if m:
        try:
            gr = float(m.group(1).replace(",", ".")) * 1000
            massa = str(int(gr)) if gr == int(gr) else str(gr)
        except ValueError:
            pass
        rest = (rest[:m.start()] + " " + rest[m.end():]).strip()
        return massa, rest

    # Граммы с явной единицей: 650 г / 650г / 650 гр / 330g
    m = re.search(r"(\d{2,4})\s*(?:г|гр|g)\b", rest, re.IGNORECASE)
    if m:
        massa = m.group(1)
        rest = (rest[:m.start()] + " " + rest[m.end():]).strip()
        return massa, rest

    # Голое число 2-4 цифры (часто это масса): 650, 500, 90
    m = re.search(r"\b(\d{2,4})\b", rest)
    if m:
        massa = m.group(1)
        rest = (rest[:m.start()] + " " + rest[m.end():]).strip()
    return massa, rest


def izvlech_oformlenie(text: str) -> tuple[str, str, float]:
    """Найти код оформления (канон базы) в тексте заказа.
    Возвращает (kanon, текст_без_оформления, уверенность 0..1).
    Если ничего не найдено — ('', исходный_текст, 0.0)."""
    nt = _norm(text)
    best_kanon, best_syn, best_len = "", "", 0

    for kanon, syns in _SINONIMY_NORM.items():
        for syn in syns:
            if not syn:
                continue
            # ищем синоним как последовательность слов (с границами)
            pattern = r"\b" + re.escape(syn).replace(r"\ ", r"\s+") + r"\b"
            if re.search(pattern, nt):
                if len(syn) > best_len:
                    best_kanon, best_syn, best_len = kanon, syn, len(syn)
                break

    if not best_kanon:
        return "", text, 0.0

    # вырезаем найденный синоним из исходного текста (приблизительно — по словам)
    # уверенность: чем длиннее совпавший синоним, тем выше
    conf = min(1.0, 0.5 + best_len / 30.0)
    # удаляем слова синонима из текста.
    # Сначала разбиваем токены со слешами («мол/атм» → «мол»,«атм»),
    # иначе окно не совпадёт с синонимом из нескольких слов.
    syn_words = best_syn.split()
    tokens = []
    for tok in text.split():
        parts = re.split(r"[./\\,]", tok)
        tokens.extend(p for p in parts if p)
    norm_tokens = [_norm(t) for t in tokens]
    out = []
    i = 0
    while i < len(tokens):
        window = " ".join(norm_tokens[i:i+len(syn_words)])
        if window == " ".join(syn_words):
            i += len(syn_words)
            continue
        out.append(tokens[i])
        i += 1
    return best_kanon, " ".join(out), conf


def razobrat_stroku_zakaza(text: str) -> dict:
    """Полный разбор строки заказа: вычленить чистое имя, оформление, массу.
    Возвращает {'imya', 'oformlenie', 'massa', 'conf_of'}."""
    massa, rest = izvlech_massu(text)
    oform, rest2, conf = izvlech_oformlenie(rest)
    imya = re.sub(r"\s+", " ", rest2).strip()
    return {
        "imya": imya,
        "oformlenie": oform,
        "massa": massa,
        "conf_of": conf,
    }
